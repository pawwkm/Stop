namespace Topz.ArmV6Z
{
    /// <summary>
    /// Specific bits of an instruction.
    /// </summary>
    internal enum Bit
    {
        /// <summary>
        /// The instruction has no bits turned on.
        /// </summary>
        None,

        /// <summary>
        /// The L bit is turned on.
        /// </summary>
        L,
        
        /// <summary>
        /// The S bit is turned on.
        /// </summary>
        S
    }
}